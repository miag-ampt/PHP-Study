/*
if-elseif-else
・if構文は女見識の評価が真の時にそれに続く文を実行するための制御構文。

if (条件式)
    文

のように表す。
条件が真の時はそれに続く文を、義の時には別の文を実行したい場合はelse

if (条件式)
    文1
else
    文2

また、条件が複数存在し、それぞれの条件に対して文を実行するような複雑な女見聞記hでは、elseifを用いて列挙することが出来る。

if (条件式1)
    文1
elseif (条件式2)
    文2
    (elseif いくつでも...)
else
    文N

if-elseif-else構文は、条件式1の評価が真の場合は文1を実行。
義の場合には条件式2を評価し、真の場合は文2を実行。
これをelseifがあるだけ繰り返し、いずれの条件式のも真にならない場合、elseの文Nを実行する。

条件式には、定数・変数・比較演算・関数呼び出しなど、式であればどのようなものも記述できる。
